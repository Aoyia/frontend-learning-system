let totalReplacements = 0;
let totalApplied = 0;

for (const [key, config] of Object.entries(files)) {
  if (!fs.existsSync(config.path)) {
    console.error(`[ERROR] 文件不存在: ${config.path}`);
    continue;
  }
  let content = fs.readFileSync(config.path, 'utf8');
  let fileApplied = 0;

  for (const replacement of config.replacements) {
    totalReplacements++;
    if (content.includes(replacement.old)) {
      content = content.replace(replacement.old, replacement.new);
      fileApplied++;
      totalApplied++;
    }
  }

  if (fileApplied > 0) {
    fs.writeFileSync(config.path, content, 'utf8');
    console.log(`[SUCCESS] [${key}] 成功替换了 ${fileApplied}/${config.replacements.length} 个项目。`);
  } else {
    console.warn(`[WARN] [${key}] 未能匹配替换任何项目。`);
  }
}

console.log(`[SUMMARY] 整体处理完毕，总共成功匹配并替换 ${totalApplied}/${totalReplacements} 个项目。`);
